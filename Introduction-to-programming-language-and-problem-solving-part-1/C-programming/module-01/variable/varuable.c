#include<stdio.h>
int main(){
    int rahim = 10;
    float karim = 10000.52;
    char habib = 'Z';
    printf("%d %f %c\n",rahim,karim,habib);

    printf("%f\n",karim);
    printf("%0.3f",karim);
    
    return 0; 
}
//format specifiers
//int =>%d
//float => %f
///char => %c
// %0.2f => 0.2 here means after decimal take two values.